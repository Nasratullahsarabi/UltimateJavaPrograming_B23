package day03_Variables;

public class DottedDiamond {
    public static void main(String[] args) {
        System.out.println("      ^");
        System.out.println("     / \\");
        System.out.println("    / * \\");
        System.out.println("   / * * \\");
        System.out.println("  / * * * \\");
        System.out.println("  ---------");
        System.out.println("  \\ * * * /");
        System.out.println("   \\ * * /");
        System.out.println("    \\ * /");
        System.out.println("     \\ /");
        System.out.println("      v");
    }
}
