package day22_loopRecap;

public class doWhileLoop {
    public static void main(String[] args) {
        int i = 10;

        while (i < 5){
            System.out.println("hello cybertek");
        }
        System.out.println("==========================================");
        do {
            System.out.println("hello cybertek");
        }while (i<5);
    }
}
