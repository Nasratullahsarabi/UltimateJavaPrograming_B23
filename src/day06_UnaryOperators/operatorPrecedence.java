package day06_UnaryOperators;

public class operatorPrecedence {

    public static void main(String[] args) {
        int a = 10,
                b = 2;
        System.out.println("Addition: "+ (a+b));
        System.out.println("Addition: "+ (a-b));
        System.out.println("Addition: "+ a*b);
        System.out.println("Addition: "+ a/b);
        System.out.println("Addition: "+ a%b);

    }
}
